const locationCoords = require("./locationCoords");

// Extract unique locations from both fixed routes and OSRM coordinate map
function getAllLocations() {
  const locations = new Set();

  routes.forEach((route) => {
    locations.add(route.from);
    locations.add(route.to);
  });

  Object.keys(locationCoords).forEach((location) => {
    locations.add(location);
  });

  return Array.from(locations).sort((a, b) => a.localeCompare(b));
}

function findFixedRoute(from, to) {
  return routes.find((r) => r.from === from && r.to === to) || null;
}

